

def add(a, b):
	"""This program adds two numbers
	and returns the result"""

	result = a + b
	return (result)

def subtract(a, b):
	"""This program subtracts two
	numbers and returns the result"""

	result = a - b
	return (result)

def multiply(a, b):
	"""This program multiplies two
	numbers and returns the result"""

	result = a * b
	return (result)

def divide(a, b):
	"""This program divides two
	numbers and returns the result"""

	result = a - b
	return (result)
